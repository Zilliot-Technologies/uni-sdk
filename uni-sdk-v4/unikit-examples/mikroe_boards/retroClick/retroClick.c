void dispWriteReg ( c8800retro_cfg_t *ctx, uint8_t reg, uint8_t data_in )
{
    uint8_t tx_buf[ 2 ];
   
    tx_buf[ 0 ] = reg;
    tx_buf[ 1 ] = data_in;
    
    i2c_write_blocking(ctx->inst,ctx->i2c_address,tx_buf,2,false);
}

int dispReadReg ( c8800retro_cfg_t *ctx, uint8_t reg, uint8_t *data_out, uint8_t len )
{
    if ( reg < C8800RETRO_REG_DIGIT_0 || 
         ( reg > C8800RETRO_REG_KEYB && reg != C8800RETRO_REG_SELF_ADDRESSING ) )
    {
        return 0;
    }
    i2c_write_blocking(ctx->inst,ctx->i2c_address,&reg,1,true);
    i2c_read_blocking(ctx->inst,ctx->i2c_address,data_out,len,false);
    return 1;
}

void dispDefaultConfig(c8800retro_cfg_t *config){
    dispWriteReg(config,C8800RETRO_REG_SHUTDOWN,C8800RETRO_NORMAL_OPERATION_RESET);
    dispWriteReg(config,C8800RETRO_REG_DECODE_MODE,C8800RETRO_NO_DECODE);
    dispWriteReg(config,C8800RETRO_REG_GLOBAL_INTENSITY,C8800RETRO_INTENSITY_16);
    dispWriteReg(config,C8800RETRO_REG_SCAN_LIMIT,C8800RETRO_SCAN_LIMIT_0_7);
}

void initDisplay(c8800retro_cfg_t *config)
{
  i2c_init(config->inst,config->i2c_speed);
  gpio_set_function(config->sda,GPIO_FUNC_I2C);
  gpio_set_function(config->scl,GPIO_FUNC_I2C);
  gpio_init(config->int_pin);
  gpio_set_dir(config->int_pin,1);
  dispDefaultConfig(config);

}

int display_char ( c8800retro_cfg_t *ctx, uint8_t char_index )
{
    uint8_t start_reg = C8800RETRO_REG_DIGIT_0;
    
    if ( char_index > 127 )
    {
        if ( char_index >= 160 )
        {
            char_index -= 32;
        }
        else
        {
            return 0;
        }
    }

    for ( uint8_t cnt = 0; cnt < 8; cnt++ )
    {
        dispWriteReg( ctx, start_reg++, font8x8_ib8x8u[ char_index ][ cnt ] );
    }
    return 1;
}

void clear_display ( c8800retro_cfg_t *ctx )
{  
    for ( uint8_t cnt = C8800RETRO_REG_DIGIT_0; cnt <= C8800RETRO_REG_DIGIT_7; cnt++ )
    {
        dispWriteReg( ctx, cnt, 0x00 );
    }
}

void display_image ( c8800retro_cfg_t *ctx, uint8_t *p_image )
{
    uint8_t start_reg = C8800RETRO_REG_DIGIT_0;
    
    for ( uint8_t cnt = 0; cnt < 8; cnt++ )
    {
        dispWriteReg( ctx, start_reg++, p_image[ cnt ] );
    }
}
  
void display_string ( c8800retro_cfg_t *ctx, uint8_t scroll_mode, uint16_t scroll_speed_ms, uint8_t *text )
{    
    uint8_t char_buf[ 256 ];
    uint8_t tmp_char_buf[ 8 ];
    uint8_t tmp_char = 0;
    uint16_t buf_len = 0;
    uint16_t buf_cnt = 0;
    uint16_t cnt = 0;


    for ( buf_cnt = 0; buf_cnt < strlen( text ); buf_cnt++ )
    {
        tmp_char = text[ buf_cnt ];
        for ( cnt = 0; cnt < 8; cnt++ )
        {
            char_buf[ buf_len++ ] = font8x8_ib8x8u[ tmp_char ][ cnt ];
        }
    }
    
    if ( C8800RETRO_SCROLL_HORIZONTAL_LEFT == scroll_mode || 
         C8800RETRO_SCROLL_VERTICAL_UP == scroll_mode )
    {
        for ( buf_cnt = 0; buf_cnt < ( buf_len - 8 ); buf_cnt++ )
        {
            if ( C8800RETRO_SCROLL_HORIZONTAL_LEFT == scroll_mode )
            {
                for ( cnt = 0; cnt < 8; cnt++ )
                {
                    tmp_char_buf[ cnt ] = ( char_buf[ buf_cnt - buf_cnt % 8 + cnt ] << ( buf_cnt % 8 ) ) | 
                                        ( char_buf[ buf_cnt - buf_cnt % 8 + cnt + 8 ] >> ( 8 - buf_cnt % 8 ) );
                }
                display_image( ctx, tmp_char_buf );
            }
            else if ( C8800RETRO_SCROLL_VERTICAL_UP == scroll_mode )
            {
                display_image( ctx, &char_buf[ buf_cnt ] );
            }
            
            for ( cnt = 0; cnt < scroll_speed_ms; cnt++ )
            {
                sleep_ms(1);
            }
        }
    }
    
    if ( C8800RETRO_SCROLL_HORIZONTAL_RIGHT == scroll_mode || 
         C8800RETRO_SCROLL_VERTICAL_DOWN == scroll_mode )
    {
        for ( buf_cnt = buf_len; buf_cnt >= 8; buf_cnt-- )
        {
            if ( C8800RETRO_SCROLL_HORIZONTAL_RIGHT == scroll_mode )
            {
                for ( cnt = 0; cnt < 8; cnt++ )
                {
                    tmp_char_buf[ cnt ] = ( char_buf[ buf_cnt - buf_cnt % 8 + cnt - 8 ] << ( buf_cnt % 8 ) ) | 
                                        ( char_buf[ buf_cnt - buf_cnt % 8 + cnt ] >> ( 8 - buf_cnt % 8 ) );
                }
                display_image( ctx, tmp_char_buf );
            }
            else if ( C8800RETRO_SCROLL_VERTICAL_DOWN == scroll_mode )
            {
                display_image( ctx, &char_buf[ buf_cnt - 8 ] );
            }
            
            for ( cnt = 0; cnt < scroll_speed_ms; cnt++ )
            {
                sleep_ms(1);
            }
        }
    }
    
}
   
