uint8_t clear_interrupt ( c8800retro_cfg_t *ctx )
{  
    uint8_t tmp_data[ 2 ] = { 0 }; 
    
    dispReadReg( ctx, C8800RETRO_REG_KEYA, tmp_data, 2 );
    return 1;
}

uint8_t get_keypad ( c8800retro_cfg_t *ctx, uint16_t *keypad )
{  
    uint8_t tmp_data[ 2 ] = { 0 }; 
    
    dispReadReg( ctx, C8800RETRO_REG_KEYA, tmp_data, 2 ); 
   
    *keypad = ( ( uint16_t ) tmp_data[ 0 ] << 8 ) | tmp_data[ 1 ];
    *keypad = ~*keypad;
    return 1;
}
